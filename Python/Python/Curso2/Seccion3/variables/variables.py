# Variables en python

# Declaracion e inicializacion de variables

edad = 24
altura = 1.73
pais = 'Guatemala'

# Acceder a las varaibles
print("Valores iniciales")
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# Modificar el valor de una variable
edad = 25
altura = 1.75
print("Valores modificados")
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# En python el tipo es dinamico
edad = 'veinticinco'
print('Edad:', edad)

# Si queremos acceder a una variable no declarada mada error
telefono = 59109961
print("Telefono:", telefono)

