# Ejemplo de tipo de datos

# Ejemplo de concatenacion o suma de valores
numero1_cadena = '10'
print(f"numero 1 en cadena:{numero1_cadena}")
numero2_cadena = '20'
print(f"Numero 2 en cadena: {numero2_cadena}")
resultado = numero1_cadena + numero2_cadena
print(f"Concatenacion: {resultado}")

# Convertimos a tipos enteros
numero1_cadena = int(numero1_cadena)
numero2_cadena = int(numero2_cadena)
resultado = numero1_cadena + numero2_cadena
print(f"Suma: {resultado}")